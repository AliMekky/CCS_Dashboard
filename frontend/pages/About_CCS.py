import streamlit as st

st.title("Welcome to CCS")

st.header("What is CCS:")
st.markdown("""
## What is Carbon Capture and Storage (CCS)?
    Carbon Capture and Storage (CCS) is a process that captures carbon dioxide (CO₂) emissions from sources like power plants and industrial processes, 
    preventing CO₂ from entering the atmosphere. The captured CO₂ is then transported and stored underground in geological formations.
""")

st.markdown("""
### Benefits of CCS
    - Reduces greenhouse gas emissions.
    - Helps mitigate climate change.
    - Supports the transition to cleaner energy sources.
""")

st.markdown("""
    ### CCS Technologies
    - **Pre-Combustion Capture:** Captures CO₂ before fuel is burned.
    - **Post-Combustion Capture:** Captures CO₂ after fuel combustion.
    - **Oxy-Fuel Combustion:** Burns fuel in oxygen instead of air for easier CO₂ capture.
""")

st.markdown("""
    ### Applications of CCS
    - Power generation
    - Cement and steel production
    - Hydrogen production
""")
